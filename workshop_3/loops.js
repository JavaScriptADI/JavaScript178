// Loops are control structures that repeat a block of code a number of times until a condition is met.
// while Loop
let i = 0;
while (i < 5) {
    console.log(i);
    i++; // i += 1;
}

/*
Output:
0
1
2
3
4
*/


// Do-While Loop
let j = 0;
do {
    console.log(j);
    j++;
} while (j < 5);

/*
Output:
0
1
2
3
4
*/

// For Loop
for (let i = 0; i < 5; i++) {
    console.log(i);
}

/*
Output:
0
1
2
3
4
*/

